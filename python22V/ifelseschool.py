marks = int(input("Enter the marks"))
if marks < 25 :
    print("The Grade F")
elif(marks >= 25 and marks < 45):
    print("The Grade E")
elif(marks >= 45 and marks < 50):
    print("The Grade D")
elif(marks >= 50 and marks < 60):
    print("The Grade C")
elif(marks >= 60 and marks < 80):
    print("The Grade B")
elif(marks >= 80 and marks < 80):
    print("The Grade A")